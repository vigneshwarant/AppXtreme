package com.avnet.storepal.beans;

public class ProductListViewBean {

	private String productName;
	private String brandName;
	private String productShortDesc;
	private int imageName;
	private String price;
	private String availableQty;
	/**
	 * @return the productName
	 */
	public String getProductName() {
		return productName;
	}
	/**
	 * @param productName the productName to set
	 */
	public void setProductName(String productName) {
		this.productName = productName;
	}
	/**
	 * @return the brandName
	 */
	public String getBrandName() {
		return brandName;
	}
	/**
	 * @param brandName the brandName to set
	 */
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	/**
	 * @return the productShortDesc
	 */
	public String getProductShortDesc() {
		return productShortDesc;
	}
	/**
	 * @param productShortDesc the productShortDesc to set
	 */
	public void setProductShortDesc(String productShortDesc) {
		this.productShortDesc = productShortDesc;
	}
	/**
	 * @return the imageName
	 */
	public int getImageName() {
		return imageName;
	}
	/**
	 * @param imageName the imageName to set
	 */
	public void setImageName(int imageName) {
		this.imageName = imageName;
	}
	/**
	 * @return the price
	 */
	public String getPrice() {
		return price;
	}
	/**
	 * @param price the price to set
	 */
	public void setPrice(String price) {
		this.price = price;
	}
	/**
	 * @return the availableQty
	 */
	public String getAvailableQty() {
		return availableQty;
	}
	/**
	 * @param availableQty the availableQty to set
	 */
	public void setAvailableQty(String availableQty) {
		this.availableQty = availableQty;
	}
	
	
	
}
